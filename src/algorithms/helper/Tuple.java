package algorithms.helper;

public class Tuple {

	public final String alpha;
	public int row;
	public final String beta;
	public int col;
	public final double distance;
	
	public Tuple(String alpha, int row, String beta, int col, double distance) {
		this.alpha = alpha;
		this.row = row;
		this.beta = beta;
		this.col = col;
		this.distance = distance;
	}
	
	
}
